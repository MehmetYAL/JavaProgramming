package day03_EscapeSequences;
/*
escape sequences: Must be given with double quote
\n: starts a new line
\t: paragraph space (tab)
\\:one back slash
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("----------------------");
        System.out.println("Hello Cydeo \nHow ar you all today? \nIt s nice to see you all. \nWhat class do we have");
        System.out.println("----------");
        System.out.println("\t Java is cool");
        System.out.println("------------------");
        System.out.println("my favorite tv show  is \"Game of Throne\"");

    }
}
