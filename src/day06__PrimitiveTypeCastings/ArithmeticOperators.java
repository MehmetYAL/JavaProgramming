package day06__PrimitiveTypeCastings;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int a= 100;
        double b = a/6d;
        System.out.println(b);

        System.out.println(1000+2000);
        System.out.println(2.5+3.5);
        System.out.println(100 - 30);
        int aa = 100 - 300;
        System.out.println(aa);
        System.out.println(20*6);
        System.out.println(5.5*7);

        System.out.println(10/4);
        System.out.println(10.0/4);
        System.out.println(10d/4);
        System.out.println(10/4d);
        System.out.println(20/6);
        System.out.println(20/6.0);
        System.out.println(10%4);



    }




}
