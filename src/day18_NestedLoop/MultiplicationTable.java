package day18_NestedLoop;

public class MultiplicationTable {
    public static void main(String[] args) {
        for (int i = 0; i <=10 ; i++) {
            System.out.println(" ");
            for (int j = 1; j <=10 ; j++) {
                System.out.print(i*j+"\t");
            }

        }
    }
}
